/**
 * Build RAG prompt for Claude (helper function for future Bedrock integration)
 */
function buildRAGPrompt(
  question: string,
  contexts: SimilaritySearchResult[],
  language: string
): string {
  const contextTexts = contexts
    .map((ctx, idx) => 
      `[Context ${idx + 1} - ${ctx.similarity_score.toFixed(2)} similarity]:\n${ctx.embedding.content_text}`
    )
    .join("\n\n");
  
  const languageInstructions: Record<string, string> = {
    mr: "कृपया मराठीत उत्तर द्या.",
    hi: "कृपया हिंदी में उत्तर दें।",
    en: "Please respond in English."
  };
  
  return `You are Kisan Setu AI, a multilingual agricultural advisor for Indian farmers.

${languageInstructions[language] || languageInstructions.en}

User Question: ${question}

Relevant Context from Knowledge Base:
${contextTexts}

Instructions:
- Provide a clear, actionable answer based on the retrieved context
- If the context is insufficient, acknowledge limitations
- Use simple, farmer-friendly language
- Include specific recommendations when possible
- Reference the source of information when relevant

Answer:`;
}

/**
 * Ingest content into RAG knowledge base
 * Generates embeddings and stores them for future retrieval
 */
export async function ingestContentForRAG(
  content: string,
  sourceType: EmbeddingSourceType,
  sourceId?: string,
  language: string = "en",
  metadata?: Record<string, unknown>
): Promise<void> {
  // Generate embedding
  const embedding = await generateEmbedding(content, language as "mr" | "hi" | "en");
  
  // Store in database
  const { storeEmbedding } = await import("./embeddingService");
  await storeEmbedding({
    source_type: sourceType,
    source_id: sourceId,
    content_text: content,
    language: language as "mr" | "hi" | "en",
    embedding,
    metadata
  });
}
